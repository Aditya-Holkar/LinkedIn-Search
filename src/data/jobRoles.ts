export function getMajorRoles(): JobRole[] {
  const roles: JobRole[] = []
  for (const dept of DEPARTMENTS) {
    for (const { level, suffix } of LEVELS) {
      const alt = LEVEL_ALT[dept][level]
      const title = alt || `${dept} ${suffix}`
      roles.push({
        id: `${dept.toLowerCase()}-${level.toLowerCase().replace(/[^a-z]/g, '')}`,
        title,
        type: 'major',
        department: dept,
        level,
      })
    }
  }
  return roles
}

export function getMinorRoles(): JobRole[] {
  return MINOR_ROLES
}

export function getAllRoles(): JobRole[] {
  return [...getMajorRoles(), ...getMinorRoles()]
}

export function getRolesByDepartment(dept: Department): { major: JobRole[]; minor: JobRole[] } {
  const all = getAllRoles()
  return {
    major: all.filter(r => r.type === 'major' && r.department === dept),
    minor: all.filter(r => r.type === 'minor' && r.department === dept),
  }
}
